package exec;

public class Study2 {
	public static void main(String[] args) {
		// 반복문

		// for
		for (int i = 0; i < 5; i++) {
			System.out.println(i);
		}

		// while
		int count = 3;
		while (count > 0) {
			System.out.println(count);
			count--;
		}

		// do-while
		int num = 0;
		do {
			System.out.println(num);
			num++;
		} while (num < 3);
	}
}
